// 获取所有项目
export function getAllProjects(): CaseStudy[] {
  return allProjects;
}

// 根据 slug 获取单个项目
export function getProjectBySlug(slug: string): CaseStudy | undefined {
  return allProjects.find((project) => project.slug === slug);
}

// 获取特色项目
export function getFeaturedProjects(): CaseStudy[] {
  return allProjects.filter((project) => project.featured);
}

// 根据分类获取项目
export function getProjectsByCategory(category: string): CaseStudy[] {
  return allProjects.filter((project) => project.category === category);
}

// 获取所有分类
export function getAllCategories(): string[] {
  const categories = allProjects.map((project) => project.category);
  return Array.from(new Set(categories));
}
